package com.josephmasini.gitexercise;

import java.util.Arrays;

public class PatternRepeater {

    /* Implement method to sort an array of integers */
    public int[] sortIntegers(int[] ints) {
        Arrays.sort(ints);
        return ints;
    }
    /* Implement method to sort an array of strings */
    public String[] sortStrings(String[] strs) {
        Arrays.sort(strs);
        return strs;
    }
}
